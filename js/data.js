// Helper to get unique categories
window.getCategories = function() {
  const cats = new Set(window.ARTICLES.map(a => a.category));
  return Array.from(cats);
};

// Helper to create article card HTML
window.createArticleCard = function(article) {
  const img = article.image
    ? `<div class="card__img"><img src="${article.image}" alt="${article.title}" loading="lazy"></div>`
    : `<div class="card__img" style="display:flex;align-items:center;justify-content:center;font-size:2rem;">${article.type === 'review' ? '&#x2B50;' : '&#x1F33F;'}</div>`;
  return `
    <a href="article.html?id=${article.id}" class="card" data-category="${article.category}">
      ${img}
      <div class="card__body">
        <span class="card__cat">${article.category}</span>
        <h3 class="card__title">${article.title}</h3>
        <p class="card__excerpt">${article.excerpt}</p>
        <div class="card__tags">${article.tags.map(t => `<span class="tag">${t}</span>`).join('')}</div>
      </div>
    </a>`;
};
